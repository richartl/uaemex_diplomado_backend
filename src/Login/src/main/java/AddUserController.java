    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("/createUser.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            UserDaoImplementation userImpl = new UserDaoImplementation();
            String email = request.getParameter("email");
            String password = request.getParameter("password");
            String name = request.getParameter("name");
            String nickname = request.getParameter("nickname");
            User newUser = new User(nickname, name, email, password);
            int id = userImpl.add(newUser);
            User createdUser = userImpl.getUser(id);
            request.setAttribute("message", "Usuario creado");
            request.setAttribute("user", createdUser);
            request.getRequestDispatcher("/success_created_user.jsp").forward(request, response);
        } catch (SQLException ex) {
            request.getRequestDispatcher("/error_create_user.jsp").forward(request, response);
        }
        
    }

